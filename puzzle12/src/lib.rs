impl<'a> Display for BingoCard<'a> {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        let mut i = 0;
        for row in self.numbers.chunks_exact(5) {
            for n in row {
                if self.marked & (1 << i) != 0 {
                    write!(f, "\x1b[31m")?;
                }
                write!(f, "{n:3}\x1b[0m ")?;
                i += 1;
            }
            writeln!(f)?;
        }
        Ok(())
    }
}

#[inline]
pub fn solve() -> (impl Display, impl Display, impl Display) {
    (solve_part1(), solve_part2(), solve_part3())
}

#[inline]
pub fn solve_part1() -> impl Display {
    let (numbers, cards) = include_str!("input.txt").split_once("\n\n").unwrap();

    let numbers = numbers.split_ascii_whitespace().map(|n| n.parse::<u16>().unwrap());
    let raw_cards = cards
        .split_ascii_whitespace()
        .map(|n| n.parse::<u16>().unwrap())
        .collect::<Vec<_>>();
    let mut cards = raw_cards
        .chunks_exact(25)
        .map(|chunk| BingoCard {
            numbers: chunk,
            marked: 0,
        })
        .collect::<Vec<_>>();

    for n in numbers {
        cards.iter_mut().for_each(|c| c.mark(n));
        if cards.iter().map(|c| c.bingos()).sum::<usize>() >= 5 {
            return n;
        }
    }

    u16::MAX
}

